import numpy as np
from matplotlib import pyplot as plt

def calculatePoly(x, a, n):

    poly = []
    j = 0

    a = np.flip(a)
    while (j < len(x)):
        result = 0
        i = 0
        while (i < n + 1):
            result += a[i] * x[j]**i
            i += 1
        poly.append(result)
        j += 1
    return poly



